<p class="ml-5 mr-5">
Dans les récits historiques, le poids des mâles sauvages aurait été de 270 à 300 kg. Cependant, l'exactitude de ces données est discutable; la taille de population des lions d'Atlas en captivité était trop petite pour pouvoir déterminer s'il s'agissait de la sous-espèce de lion la plus imposante.
Cette crinière serait due à son environnement plus tempéré de vie, et n'est donc pas un moyen d'identification fiable de la sous-espèce. Le lion de l'Atlas adulte a une longueur de 2,35 à 2,80 m3 (de longueur totale avec la queue), une hauteur de garrot de 1,20 m et pèse entre 190 et 240 kg. Les femelles mesurent jusqu'à 2,50 m (longueur totale de la tête à la queue), pour un poids de 115 à 180 kg.
 Son crâne mesure entre 30,85 cm et 37,23 cm de long.
La durée de gestation des lionnes serait de 100 à 116 jours, avec 2 à 4 petits par portée. Les lions de l'Atlas auraient une longévité de 30 ans.
Le nom scientifique Panthera leo leo désigne à présent le Lion d'Afrique.
Le lion de l'Atlas est par ailleurs l’emblème du royaume du Maroc, il est sur les armoiries de ce pays et l'équipe du Maroc est surnommée ainsi bien qu'il ne subsiste plus de lion à l'état sauvage dans ce pays.
</p>
