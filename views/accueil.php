Le Lion de l'Atlas (Panthera leo leo), également appelé Lion de Barbarie, est une sous-espèce de lion, aujourd'hui éteinte à l'état sauvage. On le trouvait autrefois dans toute l'Afrique du Nord. Les derniers spécimens sauvages ont disparu vers le milieu du xxe siècle. Moins d'une centaine de lions de l'Atlas subsistent en captivité.
